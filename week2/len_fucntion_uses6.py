# Example of using the len() function with the basic data types
# Example with a string

my_string = "Hello, World!"
print("Length of string: ", len(my_string)) #Output : 13

#Example with a listOut
my_list = [1,2,3,4,5]
print("Length of llist:",len(my_list)) #Output : 5

#Example with a tuple
my_tuple = (1,2,3,4,5)
print("Length of tuple:",len(my_tuple)) #Output : 5

# Example with a dictionary
my_dict = {'a':1, 'b' :2, 'c':3}
print("Length of dictionary:",len(my_dict)) #Output : 3 (number of keys)

# Example with a set
my_set = {1,2,3,4,5}
print("Length of set:",len(my_set)) #Output : 5